#include <stdio.h> /*printf used*/
#include "colours.h"

/*******************************************************************************
 * blue
 * Prints blue text in the terminal
 * Authors: Riza Tolentino
*******************************************************************************/
void blue () 
{
    printf("\033[1;36m");
}

/*******************************************************************************
 * red
 * Prints red text in the terminal
 * Authors: Riza Tolentino
*******************************************************************************/
void red () 
{
    printf("\033[1;31m");
}

/*******************************************************************************
 * magenta
 * Prints magenta text in the terminal
 * Authors: Riza Tolentino
*******************************************************************************/
void magenta () 
{
    printf("\033[1;35m");
}

/*******************************************************************************
 * default
 * Returns to normal colour
 * Authors: Riza Tolentino
*******************************************************************************/
void normal() {
    printf("\033[0m");
}
